package com.course.syntax;

public class StepSecond_2 {
    public static void main(String[] args) {
        int myNumber = 10;
        System.out.println(myNumber);
        myNumber--;
        System.out.println(myNumber);
        myNumber++;
        System.out.println(myNumber);
        myNumber += 10;
        System.out.println(myNumber);
        myNumber -= 5;
        System.out.println(myNumber);
        myNumber *= 7;
        System.out.println(myNumber);
        myNumber /= 3;
        System.out.println(myNumber);
        myNumber %= 3;
        System.out.println(myNumber);
    }
}
